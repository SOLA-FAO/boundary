package org.sola.services.boundary.transferobjects.casemanagement;

import java.math.BigDecimal;
import java.util.Date;
import java.util.List;
import org.sola.services.boundary.transferobjects.administrative.BaUnitBasicTO;
import org.sola.services.common.contracts.AbstractIdTO;

public class NegotiateTO extends AbstractIdTO {

    private String serviceId;
    private String baUnitId;
    private Date notificationDate;
    private BigDecimal initialAmount;
    private BigDecimal finalAmount;
    private String typeCode;
    private String statusCode;
    private String description;
    private BaUnitBasicTO baUnit;
    private List<SourceTO> sourceList;

    public NegotiateTO() {
        super();
    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public String getBaUnitId() {
        return baUnitId;
    }

    public void setBaUnitId(String baUnitId) {
        this.baUnitId = baUnitId;
    }

    public Date getNotificationDate() {
        return notificationDate;
    }

    public void setNotificationDate(Date notificationDate) {
        this.notificationDate = notificationDate;
    }

    public BigDecimal getInitialAmount() {
        return initialAmount;
    }

    public void setInitialAmount(BigDecimal initialAmount) {
        this.initialAmount = initialAmount;
    }

    public BigDecimal getFinalAmount() {
        return finalAmount;
    }

    public void setFinalAmount(BigDecimal finalAmount) {
        this.finalAmount = finalAmount;
    }

    public String getTypeCode() {
        return typeCode;
    }

    public void setTypeCode(String typeCode) {
        this.typeCode = typeCode;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public BaUnitBasicTO getBaUnit() {
        return baUnit;
    }

    public void setBaUnit(BaUnitBasicTO baUnit) {
        this.baUnit = baUnit;
    }

    public List<SourceTO> getSourceList() {
        return sourceList;
    }

    public void setSourceList(List<SourceTO> sourceList) {
        this.sourceList = sourceList;
    }
}

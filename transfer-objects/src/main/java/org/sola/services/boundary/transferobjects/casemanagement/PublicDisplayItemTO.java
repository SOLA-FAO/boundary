package org.sola.services.boundary.transferobjects.casemanagement;

import java.util.Date;
import java.util.List;
import org.sola.services.common.contracts.AbstractIdTO;

/**
 *
 * @author soladev
 */
public class PublicDisplayItemTO extends AbstractIdTO {

    private String serviceId;
    private String typeCode;
    private String referenceNr;
    private String description;
    private String statusCode;
    private Date displayFrom;
    private Date displayTo;
    private List<SourceTO> sourceList;

    public PublicDisplayItemTO() {
        super(); 
    }

    public String getServiceId() {
        return serviceId;
    }

    public void setServiceId(String serviceId) {
        this.serviceId = serviceId;
    }

    public String getTypeCode() {
        return typeCode;
    }

    public void setTypeCode(String typeCode) {
        this.typeCode = typeCode;
    }

    public String getReferenceNr() {
        return referenceNr;
    }

    public void setReferenceNr(String referenceNr) {
        this.referenceNr = referenceNr;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getStatusCode() {
        return statusCode;
    }

    public void setStatusCode(String statusCode) {
        this.statusCode = statusCode;
    }

    public Date getDisplayFrom() {
        return displayFrom;
    }

    public void setDisplayFrom(Date displayFrom) {
        this.displayFrom = displayFrom;
    }

    public Date getDisplayTo() {
        return displayTo;
    }

    public void setDisplayTo(Date displayTo) {
        this.displayTo = displayTo;
    }

    public List<SourceTO> getSourceList() {
        return sourceList;
    }

    public void setSourceList(List<SourceTO> sourceList) {
        this.sourceList = sourceList;
    }
    
}
